int		ft_checkttrmn(char	**ttrmn)
{
	int	i;
	int j;

	i = 0;
	j = 0;
	while (i < 4)
	{
		while (j < 4)
		{
			printf("%c\n", ttrmn[i][j]);
			if (ttrmn[i][j] != '.' || ttrmn[i][j] != '#')
				return (0);
			j++;
		}
		i++;
	}
	return (1);
}
